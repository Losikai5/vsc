NUMBER = int(input("ENTER THE NUMBER BTN 1-7:"))
if NUMBER == 1:
    print("SUNDAY")
elif NUMBER == 2:
    print("MONDAY")
elif NUMBER == 3:
    print("TUESDAY")
elif NUMBER == 4:
    print("WEDNESDAY")
elif NUMBER == 5:
    print("THURSDAY")
elif NUMBER == 6:
    print("FRIDAY")
elif NUMBER == 7:
    print("SATURDAY") 
else:
    print("INVALID")               

